-- ! TABLA
CREATE TABLE ACS_PERSONA (
   APR_ID             NUMBER GENERATED BY DEFAULT AS IDENTITY,
   APR_CEDULA         VARCHAR2(20) NOT NULL,
   APR_NOMBRE         VARCHAR2(200) NOT NULL,
   APR_P_APELLIDO      VARCHAR2(100) NOT NULL,
   APR_S_APELLIDO      VARCHAR2(100),
   APR_FECHA_NACIMIENTO DATE,
   
   APR_SEXO           VARCHAR2(10),
   APR_ESTADO_CIVIL   VARCHAR2(10),
   APR_TIPO_NACIONALIDAD VARCHAR2(10),
   APR_TIPO_USUARIO   VARCHAR2(15) NOT NULL,
   APR_ESTADO_REGISTRO VARCHAR2(10) DEFAULT 'PENDIENTE',
   
   APR_RESIDENCIA     VARCHAR2(255),
   APR_EMAIL          VARCHAR2(255),
   APR_TELEFONO       VARCHAR2(20),
   APR_DIRECCION_CASA VARCHAR2(255),
   APR_DIRECCION_TRABAJO VARCHAR2(255),

   APR_FECHA_CREACION DATE DEFAULT SYSDATE,
   APR_FECHA_ACTUALIZACION DATE DEFAULT SYSDATE,
   
   CONSTRAINT PK_ACS_PERSONA PRIMARY KEY (APR_ID),

   CONSTRAINT UK_APR_CEDULA UNIQUE (APR_CEDULA),
   CONSTRAINT UK_APR_EMAIL UNIQUE (APR_EMAIL),
   
   CONSTRAINT CHK_APR_SEXO CHECK (APR_SEXO IN ('MACULINO', 'FEMENINO')),
   CONSTRAINT CHK_APR_ESTADO_CIVIL CHECK (APR_ESTADO_CIVIL IN ('SOLTERO', 'CASADO', 'DIVORCIADO', 'VIUDO')),
   CONSTRAINT CHK_APR_TIPO_NACIONALIDAD CHECK (APR_TIPO_NACIONALIDAD IN ('NACIONAL', 'EXTRANJERO')),
   CONSTRAINT CHK_APR_TIPO_USUARIO CHECK (APR_TIPO_USUARIO IN ('MEDICO', 'ADMINISTRATIVO')),
   CONSTRAINT CHK_APR_ESTADO_REGISTRO CHECK (APR_ESTADO_REGISTRO IN ('PENDIENTE', 'APROBADO', 'RECHAZADO'))
);

-- * ÍNDICES
CREATE INDEX IDX_APR_NOMBRE_COMPLETO ON ACS_USUARIO (APR_NOMBRE, APR_APELLIDOS);
CREATE INDEX IDX_APR_TIPO_USUARIO ON ACS_USUARIO (APR_TIPO_USUARIO);

-- * COMENTARIOS DESCRIPTIVOS
COMMENT ON TABLE ACS_PERSONA IS 'DATOS DE SOLICITANTES A SER USUARIOS, AL ACEPTAR SE DEBE DEFINIR EL TIPO DE USUARIO';
COMMENT ON COLUMN ACS_PERSONA.APR_RESIDENCIA IS 'LUGAR GENERAL DONDE VIVE';
COMMENT ON COLUMN ACS_PERSONA.APR_DIRECCION_CASA IS 'DIRECCIÓN ESPECÍFICA DE LA CASA';
COMMENT ON COLUMN ACS_PERSONA.APR_DIRECCION_TRABAJO IS 'DIRECCIÓN ESPECÍFICA DEL TRABAJO';
COMMENT ON COLUMN ACS_PERSONA.APR_ESTADO_REGISTRO IS 'AL CAMBIAR DE ESTADO, SE DEBE CREAR EL USUARIO O RECHAZAR LA SOLICITUD. LUEGO SE BORRA EL REGISTRO';

-- ! TABLA
CREATE TABLE ACS_USUARIO (
   AUS_ID             NUMBER GENERATED BY DEFAULT AS IDENTITY,
   AUS_ESTADO         VARCHAR2(10) DEFAULT 'ACTIVO',
   
   AUS_FECHA_CREACION DATE DEFAULT SYSDATE,

   APR_ID             NUMBER,
   
   CONSTRAINT PK_ACS_USUARIO PRIMARY KEY (AUS_ID),

   CONSTRAINT FK_USUARIO_PERSONA FOREIGN KEY (APR_ID) REFERENCES ACS_PERSONA (APR_ID),
   
   CONSTRAINT CHK_AUS_ESTADO CHECK (AUS_ESTADO IN ('ACTIVO', 'INACTIVO'))
);

-- * COMENTARIOS DESCRIPTIVOS
COMMENT ON TABLE ACS_USUARIO IS 'DATOS DE USUARIO';
COMMENT ON COLUMN ACS_USUARIO.AUS_RESIDENCIA IS 'LUGAR GENERAL DONDE VIVE';
COMMENT ON COLUMN ACS_USUARIO.AUS_DIRECCION_CASA IS 'DIRECCIÓN ESPECÍFICA DE LA CASA';
COMMENT ON COLUMN ACS_USUARIO.AUS_DIRECCION_TRABAJO IS 'DIRECCIÓN ESPECÍFICA DEL TRABAJO';
COMMENT ON COLUMN ACS_USUARIO.AUS_TIPO_USUARIO IS 'DEFINE EL PERFIL DEL USUARIO, SE DEBE CREAR EL PERFIL CORRESPONDIENTE CON EL MISNMO ID DE USUARIO, NO SE PUEDE CAMBIAR';

