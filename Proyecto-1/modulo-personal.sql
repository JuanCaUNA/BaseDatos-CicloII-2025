-- * TRIGGERS
-- CREA EL USUARIO CUANDO EL ESTADO DEL PERSONA CAMBIA A 'APROBADO', ADEMAS ASIGNA EL PERFIL CORRESPONDIENTE
CREATE OR REPLACE TRIGGER TRG_AFTER_UPDATE_APR_ESR
AFTER UPDATE OF APR_ESTADO_REGISTRO ON ACS_PERSONA
FOR EACH ROW
DECLARE
   V_NEW_AUS_ID NUMBER;
BEGIN
   IF :NEW.APR_ESTADO_REGISTRO = 'APROBADO' THEN   
      INSERT INTO ACS_USUARIO (
         AUS_ID, APR_ID, AUS_FECHA_CREACION
      ) VALUES (
         :NEW.APR_ID, :NEW.APR_ID, SYSDATE
      )
      RETURNING AUS_ID INTO V_NEW_AUS_ID;

      IF :NEW.APR_TIPO_USUARIO = 'MEDICO' THEN
         INSERT INTO ACS_MEDICO (AME_ID, AME_ESTADO, AUS_ID)
         VALUES (V_NEW_AUS_ID, 'ACTIVO', V_NEW_AUS_ID);
      ELSIF :NEW.APR_TIPO_USUARIO = 'ADMINISTRATIVO' THEN
         INSERT INTO ACS_ADMINISTRATIVO (AAD_ID, AAD_ESTADO, AUS_ID)
         VALUES (V_NEW_AUS_ID, 'ACTIVO', V_NEW_AUS_ID);
      END IF;

   ELSIF :NEW.APR_ESTADO_REGISTRO = 'RECHAZADO' THEN
      DELETE FROM ACS_PERSONA WHERE APR_ID = :NEW.APR_ID;
   END IF;
END;
/

-- Trigger para actualizar APR_FECHA_ACTUALIZACION en ACS_PERSONA
CREATE OR REPLACE TRIGGER TRG_APR_UPDATE
BEFORE UPDATE ON ACS_PERSONA
FOR EACH ROW
BEGIN
   :NEW.APR_FECHA_ACTUALIZACION := SYSDATE;
END;
/

