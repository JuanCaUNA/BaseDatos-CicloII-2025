   CONSTRAINT UK_ACS_PERMISO_PA_TU UNIQUE (APE_PANTALLA, APE_TIPO_USUARIO),

   CONSTRAINT CHK_ACS_PERMISO_TIPO_USR CHECK (APE_TIPO_USUARIO IN ('MEDICO', 'ADMINISTRATIVO')),

   CONSTRAINT CHK_ACS_PERMISO_LEER CHECK (APE_LEER IN (0, 1)),
