-- ! TABLA
CREATE TABLE ACS_PERMISO (
   APE_ID        NUMBER GENERATED BY DEFAULT AS IDENTITY,
   APE_PANTALLA  VARCHAR2(100),
   APE_LEER   NUMBER(1) DEFAULT 0,
   APE_CREAR  NUMBER(1) DEFAULT 0,
   APE_EDITAR NUMBER(1) DEFAULT 0,
   APE_ELIMINAR NUMBER(1) DEFAULT 0,
   APE_TIPO_USUARIO VARCHAR2(15),
   
