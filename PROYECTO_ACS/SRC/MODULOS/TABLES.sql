-- * GLOBAL

CREATE TABLE ACS_CLAVE (
   ACL_ID       NUMBER GENERATED BY DEFAULT AS IDENTITY,
   ACL_NOMBRE_CLAVE VARCHAR2(100) UNIQUE NOT NULL,
   ACL_TIPO     VARCHAR2(50) NOT NULL, -- Correo SMTP, OTRO
   ACL_CLAVE    VARCHAR2(255) NOT NULL
);

CREATE TABLE ACS_PARAMETROS (
    APA_ID      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    APA_NOMBRE_PARAMETRO   VARCHAR2(100) UNIQUE NOT NULL,
    APA_VALOR_PARAMETRO   VARCHAR2(4000),
    APA_DESCRIPCION       VARCHAR2(200),
    APA_TIPO_PARAMETRO    VARCHAR2(20) DEFAULT 'SISTEMA',  -- Opcional: SISTEMA, EMAIL, SMTP, OTRO
);

CREATE TABLE ACL_PADRON_NACIONAL (
    APN_ID      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    APN_CEDULA  VARCHAR2(9) UNIQUE NOT NULL,
    APN_NOMBRE  VARCHAR2(30) NOT NULL,
    APN_PRIMER_APELLIDO VARCHAR2(30) NOT NULL,
    APN_SEGUNDO_APELLIDO VARCHAR2(30) NOT NULL
)
