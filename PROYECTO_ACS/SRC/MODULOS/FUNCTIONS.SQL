-- ! SE USA EN VARIOS MODULOS GLOBAL

CREATE OR REPLACE FUNCTION ACS_FUN_OBTENER_PARAMETRO(P_CLAVE VARCHAR2)
RETURN VARCHAR2 IS
    V_VALOR VARCHAR2(4000);
BEGIN
    SELECT VALOR_PARAMETRO
    INTO V_VALOR
    FROM ACS_PARAMETROS
    WHERE CLAVE_PARAMETRO = P_CLAVE;
    RETURN V_VALOR;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RETURN NULL;
END;
/
